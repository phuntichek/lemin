#include "libft.h"

void	ft_putrstr(char *str)
{
	int len;
	int i;

	len = 0;
	i = 0;
	if (str)
	{
		while (str[i])
		{
			len++;
			i++;
		}
		while (len)
			write(1, &str[len--], 1);
	}
}
