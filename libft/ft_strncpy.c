#include "libft.h"

char	*ft_strncpy(char *dest, const char *source, unsigned int n)
{
	unsigned int i;

	i = 0;
	while (source[i] != '\0' && (i < n))
	{
		dest[i] = source[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}
